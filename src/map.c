Map InitMap() {
    Map map;
    for (int y = 0; y < MAP_HEIGHT; y++)
        for (int x = 0; x < MAP_WIDTH; x++)
            map.tiles[y][x] = default_map[y][x];
    return map;
}

void DrawMap(const Map *map) {
    Texture2D wallSprite = LoadTexture("assets/sprites/biuld/UI_Flat_FrameSlot03a.png");
    for (int y = 0; y < MAP_HEIGHT; y++) {
        for (int x = 0; x < MAP_WIDTH; x++) {
            int screenX = x * TILE_SIZE;
            int screenY = y * TILE_SIZE;

            switch (map->tiles[y][x]) {
                case TILE_WALL:  DrawTexture(wallSprite, screenX, screenY, WHITE); break;
                case TILE_DESK:  DrawRectangle(screenX, screenY, TILE_SIZE, TILE_SIZE, BROWN);    break;
                case TILE_DOOR:  DrawRectangle(screenX, screenY, TILE_SIZE, TILE_SIZE, YELLOW);   break;
                default:         DrawRectangle(screenX, screenY, TILE_SIZE, TILE_SIZE, RAYWHITE); break;
            }
        
            DrawRectangleLines(screenX, screenY, TILE_SIZE, TILE_SIZE, LIGHTGRAY);
        }
    }
}

bool IsSolidTile(TileType tile) {
    return tile == TILE_WALL || tile == TILE_DESK;
}
